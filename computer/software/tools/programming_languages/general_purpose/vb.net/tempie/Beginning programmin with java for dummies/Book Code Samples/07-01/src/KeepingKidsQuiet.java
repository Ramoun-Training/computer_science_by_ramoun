class KeepingKidsQuiet {

    public static void main(String args[]) {
        int gumballs;
        int kids;
        int gumballsPerKid;
      
        gumballs = 30;      
        kids = 4;
        gumballsPerKid = gumballs / kids;
      
        System.out.print("Each kid gets ");
        System.out.print(gumballsPerKid);
        System.out.println(" gumballs.");
    }
}
